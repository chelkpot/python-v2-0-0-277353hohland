def solve():
# Ниже пишите решение задачи
    
    # Петя, Катя и Сережа делают из бумаги журавликов.
    # Петя и Сережа сделали одинаковое количество журавликов,
    # а Катя сделала в два раза больше журавликов, чем Петя и Сережа вместе.

    # Чтение входных данных
    S = int(input())

    # Решение
    # Пусть x - количество журавликов, которые сделал Петя (и Сережа)
    # Тогда Катя сделала: 2 * (x + x) = 4x
    # Всего: x + 4x + x = 6x = S
    # Отсюда x = S // 6

    x = S // 6  # количество журавликов Пети и Сережи
    katya = 4 * x  # количество журавликов Кати

    # Вывод результата  
    print(x, katya, x)  
    
# Код ниже не трогать! он нужен для тестов
if __name__ == "__main__":
    solve()