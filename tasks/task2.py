def solve():
# Ниже пишите решение задачи

    # Вася купил X карандашей, Y ручек и Z фломастеров.
    # Цена ручки на 2 рубля больше цены карандаша и на 7 рублей меньше цены фломастера.
    # Стоимость карандаша составляет 3 рубля.

    # Чтение входных данных
    X, Y, Z = map(int, input().split())

    # Цены товаров
    pencil_price = 3  # цена карандаша
    pen_price = pencil_price + 2  # цена ручки (на 2 рубля больше карандаша)
    marker_price = pen_price + 7  # цена фломастера (на 7 рублей больше ручки)

    # Расчет общей стоимости
    total_cost = (X * pencil_price) + (Y * pen_price) + (Z * marker_price)

    # Вывод результата
    print(total_cost) 

   
# Код ниже не трогать! он нужен для тестов
if __name__ == "__main__":
    solve()