def solve():
# Ниже пишите решение задачи
    
    # Гарри и Ларри стреляли по банкам с разных сторон
    # Их пули встретились в одной банке
    # a - банки, которые прострелил Гарри
    # b - банки, которые прострелил Ларри

    # Чтение входных данных
    a, b = map(int, input().split())

    # Всего банок: (a + b - 1), так как одна банка была прострелена обоими
    total_banks = a + b - 1

    # Банки, которые не успел прострелить каждый
    harry_missed = total_banks - a  # банки, которые не успел прострелить Гарри
    larry_missed = total_banks - b  # банки, которые не успел прострелить Ларри

    # Вывод результата
    print(harry_missed, larry_missed)

# Код ниже не трогать! он нужен для тестов
if __name__ == "__main__":
    solve()